public class EVA2_3_SWITCH {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
       String valor="lunes";
        switch(valor){
            case "lunes":
               
                System.out.println(1);
                 break;
            case "martes":
                System.out.println(2);
                break;
            case "miercoles":
                System.out.println(3);
                break;
            default :
                System.out.println("no valida");
        }
        
    }
    
}
